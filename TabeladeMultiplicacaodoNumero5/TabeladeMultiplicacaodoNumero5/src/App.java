public class App {
    public static void main(String[] args) {
        int numero = 5; // Número que queremos multiplicar

        // Loop de 1 a 10
        for (int i = 1; i <= 10; i++) {
            int resultado = numero * i; // Calcula o resultado da multiplicação
            System.out.println(numero + " x " + i + " = " + resultado); // Imprime o resultado na forma desejada
        }
    }
}
